// ========== HELPER FUNCTION ==========

/**
 * Fonction helper pour gérer les erreurs de chargement d'images
 * @param {Event} event - Event de l'image
 * @param {string} fallback - URL de fallback
 */
export const handleImageError = (event, fallback = PLACEHOLDERS.default) => {
  event.target.src = fallback;
  event.target.onerror = null; // Éviter la boucle infinie
};

/**
 * Fonction pour précharger une image
 * @param {string} src - URL de l'image
 * @returns {Promise}
 */
export const preloadImage = (src) => {
  return new Promise((resolve, reject) => {
    const img = new Image();
    img.onload = () => resolve(img);
    img.onerror = reject;
    img.src = src;
  });
};